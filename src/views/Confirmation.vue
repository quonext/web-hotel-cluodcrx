          case "en":
            fecha = dateArray[1] + "-" + dateArray[2] + "-" + dateArray[0];
            break;
          default:
            fecha = dateArray[2] + "-" + dateArray[1] + "-" + dateArray[0];
            break;
        }
        return fecha;
      }
